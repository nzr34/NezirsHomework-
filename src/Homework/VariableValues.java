package Homework;

public class VariableValues {
    public static void main(String[] args) {
       /*TASK:Create a Java program and name it VariableValues
        In your program create variables to store all different types of primitive data.
        Update(reassign) value of each variable
        Print the value of each variable*/
        // Declare and initialize variables for each primitive data type
        boolean myBoolean = true;
        myBoolean = false;
        byte myByte = 127;
        myByte = 111;
        short myShort = 32767;
        myShort = 22222;
        int myInt = 2023232444;
        myInt = 2023232323;
        long myLong = 9221372036855775807L;
        myLong = 1221372036855775807L;
        float myFloat = 3.3f;
        myFloat = 1.3f;
        double myDouble = 10.8765;
        myDouble = 15.5265;
        char myChar;
        myChar = 'G';

        System.out.println("myBoolean  Updated: " + myBoolean);
        System.out.println("myByte Updated    : " + myByte);
        System.out.println("myShort Updated   : " + myShort);
        System.out.println("myInt  Updated    : " + myInt);
        System.out.println("myLong Updated    : " + myLong);
        System.out.println("myFloat Updated   : " + myFloat);
        System.out.println("myDouble Updated  : " + myDouble);
        System.out.println("myChar Updated    : " + myChar);


    }
}